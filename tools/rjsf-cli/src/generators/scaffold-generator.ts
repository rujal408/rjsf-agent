// --- Grid CSS ---

function generateGridCSS(plan: FormPlanJSON, choices: ResolvedChoices): GeneratedFile | null {
  const approach = plan.stylingApproach;

  if (approach === 'tailwind' || approach === 'bare' || approach === 'mui-grid' ||
      approach === 'antd-grid' || approach === 'bootstrap-grid' || approach === 'chakra') {
    return null; // These approaches don't use a separate CSS file
  }

  const ext = approach === 'scss' ? 'scss' : 'css';
  const moduleSuffix = approach === 'plain-css' ? '' : '.module';
  const fileName = `FormGrid${moduleSuffix}.${ext}`;

  const { tablet, desktop } = choices.breakpoints;
  const { row, column } = choices.gridGap;

  const content = `/* ${fileName} — mobile-first responsive grid */
/* Generated by rjsf-cli */

.grid {
  display: grid;
  gap: ${row} ${column};
}

/* Default: 1-column (mobile) */
.cols1, .cols2, .cols3, .cols4 { grid-template-columns: 1fr; }

/* Tablet: >=${tablet} */
@media (min-width: ${tablet}) {
  .cols2 { grid-template-columns: repeat(2, 1fr); }
  .cols3 { grid-template-columns: repeat(2, 1fr); }
  .cols4 { grid-template-columns: repeat(2, 1fr); }
}

/* Desktop: >=${desktop} */
@media (min-width: ${desktop}) {
  .cols3 { grid-template-columns: repeat(3, 1fr); }
  .cols4 { grid-template-columns: repeat(4, 1fr); }
}

.colFull { grid-column: 1 / -1; }

input, select, textarea, button {
  min-height: ${choices.touchTargetSize};
}
`;

  return { path: fileName, content };
}

// --- Index.tsx Generation ---

function themeImport(theme: string): string {
  switch (theme) {
    case '@rjsf/mui': return `import Form from '@rjsf/mui';`;
    case '@rjsf/antd': return `import Form from '@rjsf/antd';`;
    case '@rjsf/bootstrap': return `import Form from '@rjsf/bootstrap-4';`;
    default: return `import Form from '@rjsf/core';`;
  }
}

function validatorImport(validator: string): string {
  if (validator === 'yup') {
    return `import { customizeValidator } from '@rjsf/validator-yup';\nconst validator = customizeValidator({});`;
  }
  return `import validator from '@rjsf/validator-ajv8';`;
}

function generateIndexTsx(plan: FormPlanJSON, choices: ResolvedChoices): string {
  const name = plan.formName;
  const noHtml5 = !choices.html5Validation;
  const showErrorList = plan.edgeCaseFlags.errorDisplay === 'inline' ? 'false' : `"top"`;

  const customWidgetImports = plan.customization.widgets
    .map((w) => `import { ${w.name} } from './widgets/${w.name}';`)
    .join('\n');
  const customFieldImports = plan.customization.fields
    .map((f) => `import { ${f.name} } from './fields/${f.name}';`)
    .join('\n');

  const widgetsObj = plan.customization.widgets.length > 0
    ? `const widgets = {\n${plan.customization.widgets.map((w) => `  ${w.name},`).join('\n')}\n};`
    : '';
  const fieldsObj = plan.customization.fields.length > 0
    ? `const fields = {\n${plan.customization.fields.map((f) => `  ${f.name},`).join('\n')}\n};`
    : '';

  return `// index.tsx — Main form component
// Generated by rjsf-cli. Use /rjsf-iterate to make changes.
import { useState } from 'react';
${themeImport(plan.rjsfTheme)}
${validatorImport(choices.validator)}
import type { IChangeEvent, ErrorSchema } from '@rjsf/utils';
import { schema } from './schema';
import { uiSchema } from './uiSchema';
import type { ${name}Data } from './types';
import { SectionTemplate } from './templates/SectionTemplate';
import './rjsf-overrides.css';
${customWidgetImports ? '\n' + customWidgetImports : ''}${customFieldImports ? '\n' + customFieldImports : ''}
${widgetsObj ? '\n' + widgetsObj + '\n' : ''}${fieldsObj ? '\n' + fieldsObj + '\n' : ''}
const templates = {
  ObjectFieldTemplate: SectionTemplate,
};

interface ${name}Props {
  formData?: Partial<${name}Data>;
  onSubmit: (data: ${name}Data) => void | Promise<void>;
  onError?: (errors: unknown) => void;
}

export function ${name}({ formData, onSubmit, onError }: ${name}Props) {
  const [status, setStatus] = useState<'idle' | 'loading' | 'success' | 'error'>('idle');
  const [serverErrors, setServerErrors] = useState<ErrorSchema<${name}Data>>({} as ErrorSchema<${name}Data>);

  const handleSubmit = async (data: IChangeEvent<${name}Data>) => {
    if (!data.formData) return;
    const submittedData = data.formData;
    setStatus('loading');
    try {
      await onSubmit(submittedData);
      setStatus('success');
    } catch (err: unknown) {
      setStatus('error');
      if (err && typeof err === 'object' && 'fieldErrors' in err) {
        const fieldErrors = (err as { fieldErrors: Record<string, string> }).fieldErrors;
        const errorSchema: Record<string, unknown> = {};
        for (const [field, msg] of Object.entries(fieldErrors)) {
          const parts = field.split('.');
          let target: Record<string, unknown> = errorSchema;
          for (let i = 0; i < parts.length - 1; i++) {
            const key = parts[i];
            if (key === undefined) continue;
            if (!target[key]) target[key] = {};
            target = target[key] as Record<string, unknown>;
          }
          const lastKey = parts[parts.length - 1];
          if (lastKey !== undefined) {
            target[lastKey] = { __errors: [msg] };
          }
        }
        setServerErrors(errorSchema as ErrorSchema<${name}Data>);
      }
      onError?.(err);
    }
  };

  if (status === 'success') {
    return <div role="status" aria-live="polite">Form submitted successfully.</div>;
  }

  return (
    <div className="rjsf-form-card">
      {status === 'loading' && <div role="status" aria-live="polite">Submitting...</div>}
      {status === 'error' && <div role="alert" style={{ color: '#dc2626', marginBottom: 16 }}>Submission failed. Please check the errors below.</div>}
      <Form<${name}Data>
        schema={schema}
        uiSchema={uiSchema}
        validator={validator}
        formData={formData}
${widgetsObj ? '        widgets={widgets}\n' : ''}${fieldsObj ? '        fields={fields}\n' : ''}        templates={templates}
        extraErrors={serverErrors}
        noHtml5Validate={${noHtml5}}
        omitExtraData={${choices.omitExtraData}}
        onSubmit={handleSubmit}
        showErrorList={${showErrorList}}
      />
    </div>
  );
}
`;
}

