// --- Schema Generation ---

function generateSchemaProperty(field: FormField, indent: string): string {
  const lines: string[] = [];

  if (field.schemaType === 'object' && field.properties) {
    lines.push(`${indent}${field.key}: {`);
    lines.push(`${indent}  type: 'object',`);
    lines.push(`${indent}  title: '${field.label}',`);
    const reqFields = field.properties.filter((f) => f.required).map((f) => `'${f.key}'`);
    if (reqFields.length > 0) {
      lines.push(`${indent}  required: [${reqFields.join(', ')}],`);
    }
    lines.push(`${indent}  properties: {`);
    for (const sub of field.properties) {
      lines.push(generateSchemaProperty(sub, indent + '    '));
    }
    lines.push(`${indent}  },`);
    lines.push(`${indent}},`);
    return lines.join('\n');
  }

  if (field.schemaType === 'array' && field.items) {
    lines.push(`${indent}${field.key}: {`);
    lines.push(`${indent}  type: 'array',`);
    lines.push(`${indent}  title: '${field.label}',`);
    if (field.validation?.minItems != null) lines.push(`${indent}  minItems: ${field.validation.minItems},`);
    if (field.validation?.maxItems != null) lines.push(`${indent}  maxItems: ${field.validation.maxItems},`);
    if (field.validation?.uniqueItems) lines.push(`${indent}  uniqueItems: true,`);
    lines.push(`${indent}  items: {`);
    if (field.items.schemaType === 'object' && field.items.properties) {
      lines.push(`${indent}    type: 'object',`);
      const itemReq = field.items.properties.filter((f) => f.required).map((f) => `'${f.key}'`);
      if (itemReq.length > 0) lines.push(`${indent}    required: [${itemReq.join(', ')}],`);
      lines.push(`${indent}    properties: {`);
      for (const sub of field.items.properties) {
        lines.push(generateSchemaProperty(sub, indent + '      '));
      }
      lines.push(`${indent}    },`);
    } else {
      lines.push(`${indent}    type: '${field.items.schemaType}',`);
    }
    lines.push(`${indent}  },`);
    lines.push(`${indent}},`);
    return lines.join('\n');
  }

  // Simple field
  lines.push(`${indent}${field.key}: {`);
  lines.push(`${indent}  type: '${field.schemaType}',`);
  lines.push(`${indent}  title: '${field.label}',`);

  if (field.format) lines.push(`${indent}  format: '${field.format}',`);
  if (field.defaultValue !== undefined) lines.push(`${indent}  default: ${JSON.stringify(field.defaultValue)},`);

  // Enum handling — always use oneOf with humanized titles
  if (field.options && field.options.length > 0) {
    const oneOf = toOneOf(field.options);
    lines.push(`${indent}  oneOf: [`);
    for (const o of oneOf) {
      lines.push(`${indent}    { const: '${o.const}', title: '${o.title}' },`);
    }
    lines.push(`${indent}  ],`);
  }

  // Validation constraints
  if (field.validation) {
    const v = field.validation;
    if (v.minLength != null) lines.push(`${indent}  minLength: ${v.minLength},`);
    if (v.maxLength != null) lines.push(`${indent}  maxLength: ${v.maxLength},`);
    if (v.pattern) lines.push(`${indent}  pattern: '${v.pattern}',`);
    if (v.minimum != null) lines.push(`${indent}  minimum: ${v.minimum},`);
    if (v.maximum != null) lines.push(`${indent}  maximum: ${v.maximum},`);
  }

  lines.push(`${indent}},`);
  return lines.join('\n');
}

function generateSchemaTs(plan: FormPlanJSON, choices: ResolvedChoices): string {
  const lines: string[] = [];
  lines.push(`// schema.ts — JSON Schema ${choices.schemaVersion}`);
  lines.push(`// Generated by rjsf-cli. Use /rjsf-iterate to make changes.`);
  lines.push(`import type { RJSFSchema } from '@rjsf/utils';`);
  lines.push('');
  lines.push(`export const schema: RJSFSchema = {`);
  lines.push(`  title: '${plan.formTitle}',`);
  lines.push(`  type: 'object',`);

  // Top-level required fields
  const allRequired: string[] = [];
  for (const section of plan.sections) {
    for (const field of section.fields) {
      if (field.required && !field.conditional) allRequired.push(field.key);
    }
  }

  if (plan.sections.length === 1) {
    // Flat schema — no section nesting
    if (allRequired.length > 0) {
      lines.push(`  required: [${allRequired.map((k) => `'${k}'`).join(', ')}],`);
    }
    lines.push(`  properties: {`);
    for (const field of plan.sections[0]!.fields) {
      lines.push(generateSchemaProperty(field, '    '));
    }
    lines.push(`  },`);
  } else {
    // Nested schema — sections as sub-objects
    const sectionKeys = plan.sections.map((s) => s.key);
    lines.push(`  required: [${sectionKeys.map((k) => `'${k}'`).join(', ')}],`);
    lines.push(`  properties: {`);
    for (const section of plan.sections) {
      lines.push(`    ${section.key}: {`);
      lines.push(`      type: 'object',`);
      lines.push(`      title: '${section.title}',`);
      const reqFields = section.fields.filter((f) => f.required && !f.conditional).map((f) => `'${f.key}'`);
      if (reqFields.length > 0) {
        lines.push(`      required: [${reqFields.join(', ')}],`);
      }
      lines.push(`      properties: {`);
      for (const field of section.fields) {
        lines.push(generateSchemaProperty(field, '        '));
      }
      lines.push(`      },`);
      lines.push(`    },`);
    }
    lines.push(`  },`);
  }

  lines.push(`};`);
  lines.push('');
  return lines.join('\n');
}

// --- UiSchema Generation ---

function generateUiSchemaTs(plan: FormPlanJSON): string {
  const lines: string[] = [];
  lines.push(`// uiSchema.ts`);
  lines.push(`// Generated by rjsf-cli. Use /rjsf-iterate to make changes.`);
  lines.push(`import type { UiSchema } from '@rjsf/utils';`);
  lines.push(`import type { ${plan.formName}Data } from './types';`);
  lines.push('');
  lines.push(`export const uiSchema: UiSchema<${plan.formName}Data> = {`);

  if (plan.sections.length === 1) {
    const section = plan.sections[0]!;
    const order = section.fields.map((f) => `'${f.key}'`);
    lines.push(`  'ui:order': [${order.join(', ')}],`);
    for (const field of section.fields) {
      lines.push(generateFieldUiSchema(field, '  '));
    }
  } else {
    const sectionOrder = plan.sections.map((s) => `'${s.key}'`);
    lines.push(`  'ui:order': [${sectionOrder.join(', ')}],`);
    for (const section of plan.sections) {
      lines.push(`  ${section.key}: {`);
      const order = section.fields.map((f) => `'${f.key}'`);
      lines.push(`    'ui:order': [${order.join(', ')}],`);
      for (const field of section.fields) {
        lines.push(generateFieldUiSchema(field, '    '));
      }
      lines.push(`  },`);
    }
  }

  lines.push(`};`);
  lines.push('');
  return lines.join('\n');
}

function generateFieldUiSchema(field: FormField, indent: string): string {
  const props: string[] = [];

  if (field.widget === 'textarea') props.push(`'ui:widget': 'textarea'`);
  if (field.widget === 'password') props.push(`'ui:widget': 'password'`);
  if (field.widget === 'radio') {
    props.push(`'ui:widget': 'radio'`);
    if (field.options && field.options.length <= 5 && field.options.every((o) => o.label.length <= 30)) {
      props.push(`'ui:options': { inline: true }`);
    }
  }
  if (field.widget === 'hidden') props.push(`'ui:widget': 'hidden'`);
  if (field.customComponent) props.push(`'ui:widget': '${field.customComponent}'`);
  if (field.placeholder) props.push(`'ui:placeholder': '${field.placeholder}'`);
  if (field.helpText) props.push(`'ui:help': '${field.helpText}'`);
  if (field.computed) props.push(`'ui:readonly': true`);

  // Extra uiSchema hints from the plan
  if (field.uiSchemaHints) {
    for (const [k, v] of Object.entries(field.uiSchemaHints)) {
      props.push(`'${k}': ${JSON.stringify(v)}`);
    }
  }

  if (props.length === 0) return '';
  if (props.length === 1) return `${indent}${field.key}: { ${props[0]} },`;
  return `${indent}${field.key}: {\n${props.map((p) => `${indent}  ${p},`).join('\n')}\n${indent}},`;
}

// --- Types Generation ---

function tsType(field: FormField): string {
  if (field.options && field.options.length > 0) {
    return toStringLiteralUnion(field.options);
  }
  switch (field.schemaType) {
    case 'string': return 'string';
    case 'number':
    case 'integer': return 'number';
    case 'boolean': return 'boolean';
    case 'array': {
      if (field.items?.schemaType === 'object') return `${capitalize(field.key)}Item[]`;
      if (field.items) return `${tsTypeFromSchemaType(field.items.schemaType)}[]`;
      return 'unknown[]';
    }
    case 'object': return capitalize(field.key);
    default: return 'unknown';
  }
}

function tsTypeFromSchemaType(type: string): string {
  switch (type) {
    case 'string': return 'string';
    case 'number':
    case 'integer': return 'number';
    case 'boolean': return 'boolean';
    default: return 'unknown';
  }
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function generateTypesTs(plan: FormPlanJSON): string {
  const lines: string[] = [];
  lines.push(`// types.ts — TypeScript interfaces derived from schema`);
  lines.push(`// Generated by rjsf-cli. Must match schema.ts properties 1:1.`);
  lines.push('');

  // Collect sub-interfaces needed
  const subInterfaces: string[] = [];

  if (plan.sections.length === 1) {
    lines.push(`export interface ${plan.formName}Data {`);
    for (const field of plan.sections[0]!.fields) {
      const opt = (!field.required || field.conditional) ? '?' : '';
      lines.push(`  ${field.key}${opt}: ${tsType(field)};`);
      collectSubTypes(field, subInterfaces);
    }
    lines.push(`}`);
  } else {
    // Root interface
    lines.push(`export interface ${plan.formName}Data {`);
    for (const section of plan.sections) {
      lines.push(`  ${section.key}: ${capitalize(section.key)};`);
    }
    lines.push(`}`);
    lines.push('');

    // Section interfaces
    for (const section of plan.sections) {
      lines.push(`export interface ${capitalize(section.key)} {`);
      for (const field of section.fields) {
        const opt = (!field.required || field.conditional) ? '?' : '';
        lines.push(`  ${field.key}${opt}: ${tsType(field)};`);
        collectSubTypes(field, subInterfaces);
      }
      lines.push(`}`);
      lines.push('');
    }
  }

  if (subInterfaces.length > 0) {
    lines.push('');
    lines.push(subInterfaces.join('\n\n'));
  }

  lines.push('');
  return lines.join('\n');
}

function collectSubTypes(field: FormField, interfaces: string[]): void {
  if (field.schemaType === 'array' && field.items?.schemaType === 'object' && field.items.properties) {
    const name = `${capitalize(field.key)}Item`;
    const lines = [`export interface ${name} {`];
    for (const sub of field.items.properties) {
      const opt = !sub.required ? '?' : '';
      lines.push(`  ${sub.key}${opt}: ${tsType(sub)};`);
    }
    lines.push('}');
    interfaces.push(lines.join('\n'));
  }
}

// --- SectionTemplate Generation ---

function generateSectionTemplate(plan: FormPlanJSON, choices: ResolvedChoices): string {
  const colConfig: string[] = [];
  const fullWidthConfig: string[] = [];

  for (const section of plan.sections) {
    colConfig.push(`  '${section.key}': ${section.columns.desktop}`);
    const fullWidth = section.fields.filter((f) => f.width === 'full').map((f) => `'${f.key}'`);
    if (fullWidth.length > 0) {
      fullWidthConfig.push(`  '${section.key}': [${fullWidth.join(', ')}]`);
    }
  }

  return `// templates/SectionTemplate.tsx
// Generated by rjsf-cli. The visual skeleton for all form sections.
import type { ObjectFieldTemplateProps } from '@rjsf/utils';
import type { ${plan.formName}Data } from '../types';

const SECTION_COLUMNS: Record<string, number> = {
${colConfig.join(',\n')},
};

const FULL_WIDTH_FIELDS: Record<string, string[]> = {
${fullWidthConfig.join(',\n')},
};

export function SectionTemplate(props: ObjectFieldTemplateProps<${plan.formName}Data>) {
  const { title, description, properties, idSchema } = props;
  const sectionId = idSchema.$id;
  const sectionKey = sectionId.replace('root_', '').split('_')[0] ?? 'root';
  const columns = SECTION_COLUMNS[sectionKey] ?? 1;
  const fullWidthFields = FULL_WIDTH_FIELDS[sectionKey] ?? [];

  // Skip rendering the root-level template wrapper if this IS the root
  if (sectionId === 'root') {
    return <div style={{ display: 'flex', flexDirection: 'column', gap: '8px' }}>{properties.map((prop) => prop.content)}</div>;
  }

  return (
    <section style={{
      border: '1px solid #e5e7eb',
      borderRadius: 10,
      padding: '24px 28px',
      marginBottom: 20,
      backgroundColor: '#fafbfc',
    }}>
      {title && (
        <h3 style={{
          fontSize: '1.05rem',
          fontWeight: 600,
          color: '#1f2937',
          marginTop: 0,
          marginBottom: description ? 4 : 20,
          paddingBottom: 12,
          borderBottom: '1px solid #e5e7eb',
        }}>
          {title}
        </h3>
      )}
      {description && (
        <p style={{ fontSize: '0.85rem', color: '#6b7280', marginTop: 0, marginBottom: 20 }}>
          {description}
        </p>
      )}
      <div style={{
        display: 'grid',
        gridTemplateColumns: \`repeat(\${columns}, 1fr)\`,
        gap: '${choices.gridGap.row} ${choices.gridGap.column}',
        alignItems: 'start',
      }}>
        {properties.map((prop) => (
          <div
            key={prop.name}
            style={{
              gridColumn: fullWidthFields.includes(prop.name) ? '1 / -1' : undefined,
            }}
          >
            {prop.content}
          </div>
        ))}
      </div>
    </section>
  );
}
`;
}

