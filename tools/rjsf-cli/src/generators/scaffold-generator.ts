// --- Main Generator ---

export async function generateScaffold(
  plan: FormPlanJSON,
  choices: ResolvedChoices
): Promise<GeneratedFile[]> {
  const files: GeneratedFile[] = [];

  // 1. SectionTemplate (FIRST — visual skeleton)
  files.push({
    path: 'templates/SectionTemplate.tsx',
    content: generateSectionTemplate(plan, choices),
  });

  // 2. CSS Overrides (SECOND) — theme-aware
  files.push({
    path: 'rjsf-overrides.css',
    content: generateOverridesCSS(choices, plan.rjsfTheme),
  });

  // 3. Grid CSS (if applicable)
  const gridFile = generateGridCSS(plan, choices);
  if (gridFile) files.push(gridFile);

  // 4. Custom widget stubs
  for (const w of plan.customization.widgets) {
    files.push(generateWidgetStub(w.name, w.forField ?? 'unknown', w.reason, plan.formName));
  }

  // 5. Custom field stubs
  for (const f of plan.customization.fields) {
    files.push(generateFieldStub(f.name, f.forField ?? 'unknown', f.reason, plan.formName));
  }

  // 6. Schema
  files.push({
    path: 'schema.ts',
    content: generateSchemaTs(plan, choices),
  });

  // 7. UiSchema
  files.push({
    path: 'uiSchema.ts',
    content: generateUiSchemaTs(plan),
  });

  // 8. Types
  files.push({
    path: 'types.ts',
    content: generateTypesTs(plan),
  });

  // 9. Index.tsx
  files.push({
    path: 'index.tsx',
    content: generateIndexTsx(plan, choices),
  });

  return files;
}
